# You can combine numbers and math operators
# Python follows the order of operations
# PEMDAS parentheses, exponents, multiplication, division, addition, subtraction
print(2 + 3 * 4)  # prints 14 (3*4 = 12, then 2+12=14)
print((2 + 3) * 4)  # prints 20 (2+3=5, then 5*4=20)

